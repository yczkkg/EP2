package stuinfo;

public class StuInfo {
	private int s_ID;
	private String s_name;
	private long s_num;
	private String s_cls;
	private int s_gra;
	private String s_maj;
	private long s_tel;
	private float s_gpa;
	private long s_bir;
	private int s_sex;
	
	public int getS_ID() {
		return s_ID;
	}
	public void setS_ID(int s_ID) {
		this.s_ID = s_ID;
	}
	public String getS_name() {
		return s_name;
	}
	public void setS_name(String s_name) {
		this.s_name = s_name;
	}
	public long getS_num() {
		return s_num;
	}
	public void setS_num(long s_num) {
		this.s_num = s_num;
	}
	public String getS_cls() {
		return s_cls;
	}
	public void setS_cls(String s_cls) {
		this.s_cls = s_cls;
	}
	public int getS_gra() {
		return s_gra;
	}
	public void setS_gra(int s_gra) {
		this.s_gra = s_gra;
	}
	public String getS_maj() {
		return s_maj;
	}
	public void setS_maj(String s_maj) {
		this.s_maj = s_maj;
	}
	public long getS_tel() {
		return s_tel;
	}
	public void setS_tel(long s_tel) {
		this.s_tel = s_tel;
	}
	public float getS_gpa() {
		return s_gpa;
	}
	public void setS_gpa(float s_gpa) {
		this.s_gpa = s_gpa;
	}
	public long getS_bir() {
		return s_bir;
	}
	public void setS_bir(long s_bir) {
		this.s_bir = s_bir;
	}
	public int getS_sex() {
		return s_sex;
	}
	public void setS_sex(int s_sex) {
		this.s_sex = s_sex;
	}
	
}
