package notice;

public class NoticeInfo {
	private int n_ID;
	private String n_num;
	private String n_info;
	private String n_time;
	public int getN_ID() {
		return n_ID;
	}
	public void setN_ID(int n_ID) {
		this.n_ID = n_ID;
	}
	public String getN_num() {
		return n_num;
	}
	public void setN_num(String n_num) {
		this.n_num = n_num;
	}
	public String getN_info() {
		return n_info;
	}
	public void setN_info(String n_info) {
		this.n_info = n_info;
	}
	public String getN_time() {
		return n_time;
	}
	public void setN_time(String n_time) {
		this.n_time = n_time;
	}
}
