package course;

public class CourseInfo {
	private int c_ID;
	private String c_name;
	private String c_cls;
	private String c_tea;
	private int c_gra;
	private String c_maj;
	private float c_cre;
	private int s_num;
	private int t_num;
	private int c_type;
	private int c_amount;
	
	public int getC_ID() {
		return c_ID;
	}
	public void setC_ID(int c_ID) {
		this.c_ID = c_ID;
	}
	public String getC_name() {
		return c_name;
	}
	public void setC_name(String c_name) {
		this.c_name = c_name;
	}
	public String getC_cls() {
		return c_cls;
	}
	public void setC_cls(String c_cls) {
		this.c_cls = c_cls;
	}
	public String getC_tea() {
		return c_tea;
	}
	public void setC_tea(String c_tea) {
		this.c_tea = c_tea;
	}
	public int getC_gra() {
		return c_gra;
	}
	public void setC_gra(int c_gra) {
		this.c_gra = c_gra;
	}
	public String getC_maj() {
		return c_maj;
	}
	public void setC_maj(String c_maj) {
		this.c_maj = c_maj;
	}
	public float getC_cre() {
		return c_cre;
	}
	public void setC_cre(float c_cre) {
		this.c_cre = c_cre;
	}
	public int getS_num() {
		return s_num;
	}
	public void setS_num(int s_num) {
		this.s_num = s_num;
	}
	public int getT_num() {
		return t_num;
	}
	public void setT_num(int t_num) {
		this.t_num = t_num;
	}
	public int getC_type() {
		return c_type;
	}
	public void setC_type(int c_type) {
		this.c_type = c_type;
	}
	public int getC_amount() {
		return c_amount;
	}
	public void setC_amount(int c_amount) {
		this.c_amount = c_amount;
	}
	
}
