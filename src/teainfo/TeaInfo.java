package teainfo;

public class TeaInfo {
	private int t_ID;
	private String t_name;
	private long t_num;
	private int t_sex;
	private String t_col;
	private long t_tel;
	
	public int getT_ID() {
		return t_ID;
	}
	public void setT_ID(int t_ID) {
		this.t_ID = t_ID;
	}
	public String getT_name() {
		return t_name;
	}
	public void setT_name(String t_name) {
		this.t_name = t_name;
	}
	public long getT_num() {
		return t_num;
	}
	public void setT_num(long t_num) {
		this.t_num = t_num;
	}
	public int getT_sex() {
		return t_sex;
	}
	public void setT_sex(int t_sex) {
		this.t_sex = t_sex;
	}
	public String getT_col() {
		return t_col;
	}
	public void setT_col(String t_col) {
		this.t_col = t_col;
	}
	public long getT_tel() {
		return t_tel;
	}
	public void setT_tel(long t_tel) {
		this.t_tel = t_tel;
	}
	
}
