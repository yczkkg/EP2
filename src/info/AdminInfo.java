package info;

public class AdminInfo {
	private int a_ID;
	private String a_name;
	private long a_num;
	private int a_sex;
	private int a_year;
	private long a_tel;
	public int getA_ID() {
		return a_ID;
	}
	public void setA_ID(int a_ID) {
		this.a_ID = a_ID;
	}
	public String getA_name() {
		return a_name;
	}
	public void setA_name(String a_name) {
		this.a_name = a_name;
	}
	public long getA_num() {
		return a_num;
	}
	public void setA_num(long a_num) {
		this.a_num = a_num;
	}
	public int getA_sex() {
		return a_sex;
	}
	public void setA_sex(int a_sex) {
		this.a_sex = a_sex;
	}
	public int getA_year() {
		return a_year;
	}
	public void setA_year(int a_year) {
		this.a_year = a_year;
	}
	public long getA_tel() {
		return a_tel;
	}
	public void setA_tel(long a_tel) {
		this.a_tel = a_tel;
	}
	
}
